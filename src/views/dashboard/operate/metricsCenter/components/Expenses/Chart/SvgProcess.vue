  import { computed } from 'vue';

  interface IProps {
    proportion: number; // 费用管理
    orgCode?: string;
  }
  const props = withDefaults(defineProps<IProps>(), {
    proportion: 0,
    orgCode: '',
  });

  const width = 135;
  const height = 68;
  const xMaxAngle = 60; // 长边的最大角度
  const yMaxAngle = 120; // 短边的最大角度

  const fareZb = computed(() => {
    const res = props.proportion.toFixed(1);
    return res;
  });
  // 倒扣产值占比角度
  const deductionAngle = computed(() => {
    const res = (Number.parseFloat(fareZb.value) / 100) * 360;
    return res;
  });

  // 蓝色路径
  const bluePath = computed(() => {
    return getPath(deductionAngle.value);
  });

  const getPath = (angle: any) => {
    if (angle === 0) return 'M90 95 L20 125Z;';
    let path = `M90 95 L20 125Z;`;
    if (angle > 0) {
      const xlen = ((angle % xMaxAngle) / xMaxAngle) * width || width;
      path = `${path}
        M90 95 L20 125 L${20 + xlen / 2} 125Z
        M90 95 L20 125 L${20 + xlen} 125Z;`;
    }
    if (angle > xMaxAngle) {
      const ylen = ((angle - xMaxAngle) / yMaxAngle) * height || height;
      path = `${path}
        M90 95 L20 125 L155 125 L155 ${125 - ylen / 2}Z;
        M90 95 L20 125 L155 125 L155 ${125 - ylen}Z;`;
    }
    if (angle > 180) {
      const xlen = ((angle - 180) / xMaxAngle) * width || width;
      path = `${path}
        M90 95 L20 125 L155 125 L155 57 L${155 - xlen / 2} 57Z;
        M90 95 L20 125 L155 125 L155 57 L20 57 L${155 - xlen} 57Z;`;
    }
    if (angle > 180 + xMaxAngle) {
      const ylen = ((angle - 180 - xMaxAngle) / yMaxAngle) * height || height;
      path = `${path}
        M90 95 L20 125 L155 125 L155 57 L20 57 L20 ${57 + ylen}Z;`;
    }
    return path;
  };
</script>
